# Test string concatenation with + operator

# Basic string concatenation
result1 = "Hello" + " " + "World"
print(result1)

# String concatenation with variables
name = "Alice"
greeting = "Hello, " + name + "!"
print(greeting)

# Multiple concatenations
first = "Python"
second = " is "
third = "awesome"
result2 = first + second + third
print(result2)

# Empty string concatenation
empty = ""
result3 = empty + "Start" + "" + "End" + ""
print(result3)

# Concatenation with special characters
result4 = "Line1\n" + "Line2\t" + "Line3"
print(result4)