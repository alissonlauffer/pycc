# Test string multiplication with * operator

# Basic string multiplication
result1 = "foo" * 2
print(result1)

# String multiplication with different counts
result2 = "hello" * 3
print(result2)

result3 = "test" * 1
print(result3)

# Edge cases
result4 = "abc" * 0
print(result4)

result5 = "xyz" * 5
print(result5)

# String multiplication with variables
base = "bar"
count = 4
result6 = base * count
print(result6)

# Multiple operations
result7 = "a" * 10
print(result7)

# Empty string multiplication
result8 = "" * 5
print(result8)

# Single character multiplication
result9 = "x" * 3
print(result9)